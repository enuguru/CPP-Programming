

// C++ program to sizes of data types 
#include<iostream> 
using namespace std; 

int main() 
{ 
	cout << endl << "Size of char : " << sizeof(char) 
	<< " byte" << endl << endl; 
	cout << "Size of unsigned char : " << sizeof(unsigned char) 
	<< " byte" << endl << endl; 
	cout << "Size of int : " << sizeof(int) 
	<< " bytes" << endl << endl; 
	cout << "Size of unsigned int : " << sizeof(unsigned int) 
	<< " bytes" << endl << endl; 
	cout << "Size of short int : " << sizeof(short int) 
	<< " bytes" << endl << endl; 
	cout << "Size of unsigned short int : " << sizeof(unsigned short int) 
	<< " bytes" << endl << endl; 
	cout << "Size of long int : " << sizeof(long int) 
	<< " bytes" << endl << endl; 
	cout << "Size of signed long int : " << sizeof(signed long int) 
	<< " bytes" << endl << endl; 
	cout << "Size of unsigned long int : " << sizeof(unsigned long int) 
	<< " bytes" << endl << endl; 
	cout << "Size of signed long long int : " << sizeof(signed long long int) 
	<< " bytes" << endl << endl; 
	cout << "Size of unsigned long long int : " << sizeof(unsigned long long int) 
	<< " bytes" << endl << endl; 
	cout << "Size of float : " << sizeof(float) 
	<< " bytes" <<endl << endl; 
	cout << "Size of double : " << sizeof(double) 
	<< " bytes" << endl << endl; 
	cout << "Size of long double : " << sizeof(long double) 
	<< " bytes" << endl << endl; 
	cout << "Size of wchar_t : " << sizeof(wchar_t) 
	<< " bytes" <<endl << endl; 
	
	return 0; 
} 
