
#include <iostream>
using namespace std;
 
int main()
{
    cout << "\nSize of char: " << sizeof(char) << " byte" << endl;
    cout << "\nSize of wchar_t: " << sizeof(wchar_t) << " bytes" << endl;
    cout << "\nSize of char16_t: " << sizeof(char16_t) << " bytes" << endl;
    cout << "\nSize of char32_t: " << sizeof(char32_t) << " bytes" << endl;
    cout << "\nSize of int: " << sizeof(int) << " bytes" << endl;
    cout << "\nSize of unsigned int: " << sizeof(unsigned int) << " bytes" << endl;
    cout << "\nSize of long int: " << sizeof(long int) << " bytes" << endl;
    cout << "\nSize of long long int: " << sizeof(long long int) << " bytes" << endl;
    cout << "\nSize of float: " << sizeof(float) << " bytes" << endl;
    cout << "\nSize of double: " << sizeof(double) << " bytes" << endl;
    cout << "\nSize of long double: " << sizeof(long double) << " bytes" << endl;
 
    return 0;
}
